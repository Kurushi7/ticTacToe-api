<?php

namespace App\Models;


/**
 * Class Cell
 * @package App\Models
 */
class Cell
{
    public $x = 0;
    public $y = 0;
    public $value = '';
    public $weight = 0;

    public function __construct($x, $y, $value, $weight)
    {
        $this->x = $x;
        $this->y = $y;
        $this->value = $value;
        $this->weight = $weight;
    }

    /**
     * @return int
     */
    public function getX(): int
    {
        return $this->x;
    }

    /**
     * @param int $x
     */
    public function setX(int $x): void
    {
        $this->x = $x;
    }

    /**
     * @return int
     */
    public function getY(): int
    {
        return $this->y;
    }

    /**
     * @param int $y
     */
    public function setY(int $y): void
    {
        $this->y = $y;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }

    /**
     * @param string $value
     */
    public function setValue(string $value): void
    {
        $this->value = $value;
    }

    /**
     * @return int
     */
    public function getWeight(): int
    {
        return $this->weight;
    }

    /**
     * @param int $weight
     */
    public function setWeight(int $weight): void
    {
        $this->weight = $weight;
    }


}
